//百度行政区划区域检索
function getLableBySearch($cityname,$key)
{
    $url = "http://api.map.baidu.com/place/v2/search?query=".urlencode($key)."&region=".urlencode($cityname)."&filter=industry_type:life&output=json&ak=".config('map_baidu_ak');
    $content = curl_file_get_contents ( $url ,86400);
    $rs = json_decode ( $content, true );
    $result = array();
    if ($rs['status']==0 && !empty($rs['results'])){
        $result = array(
            'txt' => $rs['results'][0]['name'],
            'lat' => $rs['results'][0]['location']['lat'],
            'lng' => $rs['results'][0]['location']['lng']
        );
    }else{
        $result = array(
            'txt' => '',
            'lat' => 0,
            'lng' => 0
        );
    }
    return $result;
}

