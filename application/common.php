/**
 * curl POST文件
 * @param type $url
 * @param type $postField
 * @param type $timeout
 * @return type
 */
function curl_post_contents($url, $postField, $timeout = 30)
{
    $ch = curl_init ();
    curl_setopt ( $ch, CURLOPT_URL, $url );
    curl_setopt ( $ch, CURLOPT_POST, 1 );
    curl_setopt ( $ch, CURLOPT_POSTFIELDS, http_build_query($postField) );
    curl_setopt ( $ch, CURLOPT_TIMEOUT, $timeout );
    if (isset ( $_SERVER ['HTTP_USER_AGENT'] ))
        curl_setopt ( $ch, CURLOPT_USERAGENT, $_SERVER ['HTTP_USER_AGENT'] );
    curl_setopt ( $ch, CURLOPT_RETURNTRANSFER, 1 );
    $r = curl_exec ( $ch );
    curl_close ( $ch );
    return $r;
}

//获取距离
function getDistance($lng1, $lat1, $lng2, $lat2, $flag = "xy")
{
    if ($flag == 'xy') {
        $lat1 = $lat1 / 100000;
        $lng1 = $lng1 / 100000;
        $lat2 = $lat2 / 100000;
        $lng2 = $lng2 / 100000;
    }
    $EARTH_RADIUS = 6378.137;
    $radLat1 = $lat1 * pi() / 180.0;
    $radLat2 = $lat2 * pi() / 180.0;
    $a = $radLat1 - $radLat2;
    $b = ($lng1 * pi() / 180.0) - ($lng2 * pi() / 180.0);
    $s = 2 * asin(sqrt(pow(sin($a / 2), 2) + cos($radLat1) * cos($radLat2) * pow(sin($b / 2), 2)));
    $s = $s * $EARTH_RADIUS;
    $s = round($s * 10000) / 10000;
    return (string)number_format($s, 2);
}

/**
+----------------------------------------------------------
 * 取得字符串的长度，支持中文和其他编码
+----------------------------------------------------------
 * @static
 * @access public
+----------------------------------------------------------
 * @param string $str 字符串
 * @param string $charset 编码格式
+----------------------------------------------------------
 * @return string
+----------------------------------------------------------
 */
function mstrlen($str, $charset="utf-8"){
    if(function_exists("mb_substr"))
        $length = mb_strlen($str, $charset);
    elseif(function_exists('iconv_substr')) {
        $length = iconv_strlen($str, $charset);
    }else{
        $re['utf-8']   = "/[\x01-\x7f]|[\xc2-\xdf][\x80-\xbf]|[\xe0-\xef][\x80-\xbf]{2}|[\xf0-\xff][\x80-\xbf]{3}/";
        $re['gb2312'] = "/[\x01-\x7f]|[\xb0-\xf7][\xa0-\xfe]/";
        $re['gbk']    = "/[\x01-\x7f]|[\x81-\xfe][\x40-\xfe]/";
        $re['big5']   = "/[\x01-\x7f]|[\x81-\xfe]([\x40-\x7e]|\xa1-\xfe])/";
        preg_match_all($re[$charset], $str, $match);
        $length = count($match[0]);
    }
    return $length;
}


/**
 * +----------------------------------------------------------
 * 字符串截取，支持中文和其他编码
 * +----------------------------------------------------------
 *
 * @static
 *
 * @access public
 * +----------------------------------------------------------
 * @param string $str
 * 需要转换的字符串
 * @param string $length
 * 截取长度
 * @param string $start
 * 开始位置
 * @param string $charset
 * 编码格式
 * @param string $suffix
 * 截断显示字符
 * +----------------------------------------------------------
 * @return string
 * +----------------------------------------------------------
 */
function msubstr($str, $length, $start = 0, $suffix = false , $charset = "utf-8")
{
    if (function_exists ( "mb_substr" ))
        $slice = mb_substr ( $str, $start, $length, $charset );
    elseif (function_exists ( 'iconv_substr' )) {
        $slice = iconv_substr ( $str, $start, $length, $charset );
    } else {
        $re ['utf-8'] = "/[\x01-\x7f]|[\xc2-\xdf][\x80-\xbf]|[\xe0-\xef][\x80-\xbf]{2}|[\xf0-\xff][\x80-\xbf]{3}/";
        $re ['gb2312'] = "/[\x01-\x7f]|[\xb0-\xf7][\xa0-\xfe]/";
        $re ['gbk'] = "/[\x01-\x7f]|[\x81-\xfe][\x40-\xfe]/";
        $re ['big5'] = "/[\x01-\x7f]|[\x81-\xfe]([\x40-\x7e]|\xa1-\xfe])/";
        preg_match_all ( $re [$charset], $str, $match );
        $slice = join ( "", array_slice ( $match [0], $start, $length ) );
    }
    if ($suffix && strlen ( $str ) > strlen ( $slice ))
        return $slice . "…";
    return $slice;
}

